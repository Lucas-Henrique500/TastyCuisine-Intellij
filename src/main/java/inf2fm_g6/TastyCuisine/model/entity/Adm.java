package inf2fm_g6.TastyCuisine.model.entity;

public class Adm {

    private String nomeAdm;
    private int codModerador;
    private String gmail;
    private String senha;

    public String getNomeAdm() {
        return nomeAdm;
    }

    public void setNomeAdm(String nomeAdm) {
        this.nomeAdm = nomeAdm;
    }

    public int getCodModerador() {
        return codModerador;
    }

    public void setCodModerador(int codModerador) {
        this.codModerador = codModerador;
    }

    public String getGmail() {
        return gmail;
    }

    public void setGmail(String gmail) {
        this.gmail = gmail;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
}
